def main():
     num1 = int(input("Ingrese numero1: "))
     num2 = int(input("Ingrese numero2: "))
     print("Los resultados para {} y {} son:".format(num1,num2))
     print("Suma: {}".format(num1 + num2))
     print("Resta: {}".format(num1 - num2))
     print("Divi: {}".format(num1 / num2))
     print("Multi: {}".format(num1 * num2))
        
main()